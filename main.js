let gameWidth;
let gameHeight;    
let gameScreen;
let gameFrame = 0;
let gameImageMap;
let playerImage;
let playerX = 10;
let playerY = 5;

function drawMain() {
    const game = gameScreen.getContext("2d");

    for (let y = 0; y < 20; y++) {
        for (let x = 0; x < 20; x++) {
            let px = playerX + x;
            let py = playerY + y;
            drawTile(
                game, 
                x * TILE_SIZE, 
                y * TILE_SIZE, 
                GAME_MAP[py * MAP_WIDTH + px]);            
        }
    }

    game.drawImage(
        playerImage, 
        PLAYER_W, 
        0, 
        PLAYER_W, 
        PLAYER_H, 
        WIDTH / 2, 
        HEIGHT / 2, 
        PLAYER_W, PLAYER_H
    );
    // game.font = FONT;
    // game.fillText("Dragn Waryer" + gameFrame, gameFrame / 10, 64);
}


function drawTile(game, x, y, index) {
    const xIndex = (index % TILE_COLUMN) * TILE_SIZE;
    const yIndex = Math.floor(index / TILE_ROW) * TILE_SIZE;
    game.drawImage(
        gameImageMap, 
        xIndex, 
        yIndex, 
        TILE_SIZE, 
        TILE_SIZE, 
        x, 
        y, 
        TILE_SIZE, 
        TILE_SIZE
    );
}


function loadImage() {
    gameImageMap = new Image(); 
    gameImageMap.src = MAP_IMAGE_PATH;
    playerImage = new Image();
    playerImage.src = PLAYER_IMAGE_PATH;
}

function wmPaint() {
    drawMain();

    const canvas = document.getElementById("main");
    const game = canvas.getContext("2d");

    game.drawImage(
            gameScreen, 
            0, 
            0, 
            gameScreen.width, 
            gameScreen.height, 
            0, 
            0, 
            gameWidth, 
            gameHeight
    );
}


function wmSize() {
    const canvas = document.getElementById("main");
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;

    const game = canvas.getContext("2d");
    game.ImageSmoothingEnabled = game.msImageSmoothingEnabled = SMOOTH;

    gameWidth = canvas.width;
    gameHeight = canvas.height;

    if (gameWidth / WIDTH < gameHeight / HEIGHT) {
        gameHeight = gameWidth * HEIGHT / WIDTH;
    } else {
        gameWidth = gameHeight * WIDTH / HEIGHT;
    }
}


function wmTimer() {
    gameFrame++;
    wmPaint();
}

window.onload = function() {
    loadImage()

    gameScreen = document.createElement("canvas");
    gameScreen.width = WIDTH;
    gameScreen.height = HEIGHT;

    wmSize();
    window.addEventListener("resize", function() {wmSize()});

    setInterval(function() { wmTimer() }, 33);
}